package com.vehicle;

public class VehicleIndetifiers {
	
	
	private String frame;
	
	private String wheel;
	
	private String powertrain;
	
	private String vechileIdentification;

	public String getFrame() {
		return frame;
	}

	public void setFrame(String frame) {
		this.frame = frame;
	}

	public String getWheel() {
		return wheel;
	}

	public void setWheel(String wheel) {
		this.wheel = wheel;
	}

	public String getPowertrain() {
		return powertrain;
	}

	public void setPowertrain(String powertrain) {
		this.powertrain = powertrain;
	}

	public String getVechileIdentification() {
		return vechileIdentification;
	}

	public void setVechileIdentification(String vechileIdentification) {
		this.vechileIdentification = vechileIdentification;
	}
	
	
	
	

}
